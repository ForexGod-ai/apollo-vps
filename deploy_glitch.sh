# PHASE 2: FILESYSTEM ARCHITECTURE
# ═══════════════════════════════════════════════════════════════════════════════
phase_filesystem() {
    log STEP "PHASE 2/5 — FILESYSTEM ARCHITECTURE"

    # Required directories
    local dirs=(
        "${SCRIPT_DIR}/logs"
        "${SCRIPT_DIR}/data"
        "${SCRIPT_DIR}/backups"
        "${SCRIPT_DIR}/charts"
        "${SCRIPT_DIR}/chart_snapshots"
        "${SCRIPT_DIR}/archive"
    )

    for dir in "${dirs[@]}"; do
        mkdir -p "${dir}"
        log OK "Directory: $(basename "${dir}")/"
    done

    # Apollo permission — ensure cTrader can read/write signals.json
    log INFO "Setting permissions for cTrader IPC bridge..."
    chmod 777 "${SCRIPT_DIR}" 2>/dev/null || true
    # Ensure signals.json is accessible
    touch "${SCRIPT_DIR}/signals.json" 2>/dev/null || true
    chmod 666 "${SCRIPT_DIR}/signals.json" 2>/dev/null || true
    log OK "Apollo directory: chmod 777 (cTrader IPC bridge ready)"

    # Ensure critical state files exist
    local state_files=(
        "monitoring_setups.json"
        "signals.json"
        "active_positions.json"
        "trade_confirmations.json"
    )

    for sf in "${state_files[@]}"; do
        local fpath="${SCRIPT_DIR}/${sf}"
        if [[ ! -f "${fpath}" ]]; then
            echo "[]" > "${fpath}"
            log WARN "Created missing state file: ${sf} (initialized as [])"
        else
            log OK "State file exists: ${sf} ($(wc -c < "${fpath}" | tr -d ' ')B)"
        fi
    done

    # .env check
    if [[ -f "${SCRIPT_DIR}/.env" ]]; then
        local env_keys
        env_keys=$(grep -c "=" "${SCRIPT_DIR}/.env" 2>/dev/null || echo "0")
        log OK ".env file: ${env_keys} keys configured"
    else
        log FAIL ".env file not found! Telegram & cTrader will not work."
        log INFO "Create .env with: TELEGRAM_BOT_TOKEN, TELEGRAM_CHAT_ID, CTRADER_*"
        return 1
    fi
}

