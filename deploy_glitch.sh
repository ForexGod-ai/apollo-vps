# PHASE 4: WATCHDOG TRIGGER
# ═══════════════════════════════════════════════════════════════════════════════
phase_watchdog() {
    log STEP "PHASE 4/5 — WATCHDOG TRIGGER"

    # Verify all subsystem scripts exist
    local subsystems=(
        "watchdog_monitor.py"
        "setup_executor_monitor.py"
        "position_monitor.py"
        "realtime_monitor.py"
        "telegram_command_center.py"
        "ctrader_sync_daemon.py"
        "news_calendar_monitor.py"
        "daily_scanner.py"
    )

    local missing=0
    for sub in "${subsystems[@]}"; do
        if [[ -f "${SCRIPT_DIR}/${sub}" ]]; then
            log OK "Subsystem present: ${sub}"
        else
            log FAIL "MISSING subsystem: ${sub}"
            missing=$((missing + 1))
        fi
    done

    if [[ ${missing} -gt 0 ]]; then
        log FAIL "${missing} subsystem(s) missing — cannot proceed"
        return 1
    fi

    # Verify loguru file logging is configured in watchdog
    if grep -q "logger.add" "${SCRIPT_DIR}/watchdog_monitor.py" 2>/dev/null; then
        log OK "Watchdog: loguru file rotation configured"
    else
        log WARN "Watchdog: loguru file logging NOT detected"
    fi

    # Verify watchdog stdout/stderr redirect (not DEVNULL)
    if grep -q "stdout_log\|stderr_log" "${SCRIPT_DIR}/watchdog_monitor.py" 2>/dev/null; then
        log OK "Watchdog: stdout/stderr → forensic log files"
    else
        log WARN "Watchdog: may still use DEVNULL for subprocess output"
    fi

    # Syntax-check all subsystems before launching
    log INFO "Syntax-checking all subsystems..."
    local syntax_ok=0
    local syntax_fail=0
    for sub in "${subsystems[@]}"; do
        if "${PYTHON}" -c "import py_compile; py_compile.compile('${SCRIPT_DIR}/${sub}', doraise=True)" 2>>"${DEPLOY_LOG}"; then
            syntax_ok=$((syntax_ok + 1))
        else
            log FAIL "Syntax error in: ${sub}"
            syntax_fail=$((syntax_fail + 1))
        fi
    done
    log OK "Syntax check: ${syntax_ok}/${#subsystems[@]} passed"

    if [[ ${syntax_fail} -gt 0 ]]; then
        log FAIL "${syntax_fail} file(s) have syntax errors — fix before launching"
        return 1
    fi

    # Show launch readiness (don't auto-start — operator decides)
    echo ""
    echo -e "  ${CYAN}${BOLD}┌─────────────────────────────────────────────────┐${NC}"
    echo -e "  ${CYAN}${BOLD}│  WATCHDOG LAUNCH COMMAND:                       │${NC}"
    echo -e "  ${CYAN}${BOLD}│                                                 │${NC}"
    echo -e "  ${CYAN}${BOLD}│${NC}  ${WHITE}nohup ${PYTHON} watchdog_monitor.py &${NC}             ${CYAN}${BOLD}│${NC}"
    echo -e "  ${CYAN}${BOLD}│                                                 │${NC}"
    echo -e "  ${CYAN}${BOLD}│  ${DIM}Watchdog auto-starts all 6 subsystems${NC}        ${CYAN}${BOLD}│${NC}"
    echo -e "  ${CYAN}${BOLD}│  ${DIM}+ Daily Scanner via cron at 00:05 UTC${NC}        ${CYAN}${BOLD}│${NC}"
    echo -e "  ${CYAN}${BOLD}└─────────────────────────────────────────────────┘${NC}"
}

