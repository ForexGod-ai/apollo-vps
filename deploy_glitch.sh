# PHASE 3: GLOBAL UTC SYNC
# ═══════════════════════════════════════════════════════════════════════════════
phase_timezone() {
    log STEP "PHASE 3/5 — GLOBAL UTC SYNC"

    # Verify TZ=UTC is forced in all critical Python files
    local critical_files=(
        "setup_executor_monitor.py"
        "position_monitor.py"
        "watchdog_monitor.py"
        "realtime_monitor.py"
        "telegram_command_center.py"
        "ctrader_executor.py"
        "daily_scanner.py"
    )

    local tz_count=0
    for cf in "${critical_files[@]}"; do
        if grep -q "os.environ\['TZ'\]" "${SCRIPT_DIR}/${cf}" 2>/dev/null; then
            tz_count=$((tz_count + 1))
        else
            log WARN "TZ=UTC NOT forced in: ${cf}"
        fi
    done

    log OK "TZ=UTC enforcement: ${tz_count}/${#critical_files[@]} Python processes"

    # Runtime verification (uses datetime.timezone.utc for cross-platform reliability)
    local tz_test
    tz_test=$("${PYTHON}" -c "
import os, time
os.environ['TZ'] = 'UTC'
try:
    time.tzset()
except AttributeError:
    pass
from datetime import datetime, timezone
utc_now = datetime.now(timezone.utc)
local_now = datetime.now()
drift = abs((utc_now.replace(tzinfo=None) - local_now).total_seconds())
tz_label = 'UTC' if drift < 2 else f'DRIFT_{drift:.0f}s'
print(f'{tz_label}|{utc_now.strftime(\"%Y-%m-%d %H:%M:%S\")} UTC')
" 2>/dev/null)

    local tz_zone
    tz_zone=$(echo "${tz_test}" | cut -d'|' -f1)
    local tz_time
    tz_time=$(echo "${tz_test}" | cut -d'|' -f2)

    if [[ "${tz_zone}" == "UTC" ]]; then
        log OK "Runtime TZ verification: ${tz_zone} — ${tz_time}"
    else
        log FAIL "TZ verification failed: got '${tz_zone}' — local time drifts from UTC"
        return 1
    fi
}

