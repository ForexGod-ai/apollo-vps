log() {
    local level="$1"; shift
    local msg="$*"
    local ts
    ts=$(date -u '+%Y-%m-%d %H:%M:%S')
    echo "[${ts}] [${level}] ${msg}" >> "${DEPLOY_LOG}"
    case "${level}" in
        OK)    echo -e "  ${OK}  ${msg}" ;;
        FAIL)  echo -e "  ${FAIL}  ${msg}" ;;
